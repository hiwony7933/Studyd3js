var highlight = function (d) {
  selected_specie = d.Species;

  d3.selectAll(".dot") //
      .transition()
      .duration(200)
      .attr("r", 1);

  d3.selectAll("." + selected_specie)
      .transition()
      .duration(200)
      .style("fill", color(selected_specie))
      .attr("r", 7);
};

